import User from "../models/userModel.js";
import asyncHandler from "../middlewares/asyncHandler.js";
import bcrypt from "bcryptjs";
import createToken from "../utils/createToken.js";

const createUser = asyncHandler(async (req, res) => {
    const { username, email, password } = req.body;

     // Message to user if username, email, or password are not filled in.
  if (!username || !email || !password) {
    throw new Error("Please fill in all fields.");
  }
   // Message to user if user email already exists.
  const userExists = await User.findOne({ email });
  if (userExists) res.status(400).send("Email already exists");
  
  // Hash user password
  const salt = await bcrypt.genSalt(10);
  const hashedPassword = await bcrypt.hash(password, salt);
  // Create new User document and pass in user properties
  const newUser = new User({ username, email, password: hashedPassword });

  // Database error handling
  try {
    await newUser.save(); // Save user data
    createToken(res, newUser._id);

    res.status(201).json({
      _id: newUser._id,
      username: newUser.username,
      email: newUser.email,
      isAdmin: newUser.isAdmin,
    });
  } catch (error) {
    res.status(400);
    throw new Error("Invalid user data");
  }
});
